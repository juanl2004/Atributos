package ejercicio1;

public class ClaseEj1 {

	double num1, num2;

	public ClaseEj1() {
	}
	

	public ClaseEj1(double num1, double num2) {
		super();
		this.num1 = num1;
		this.num2 = num2;
	}


	public double suma() {
		return num1 + num2;
	}

	public double resta() {
		return num1 - num2;
	}

	public double multiplicacion() {
		return num1 * num2;
	}

	public double division() {
		return num1 * num2;
	}
}
